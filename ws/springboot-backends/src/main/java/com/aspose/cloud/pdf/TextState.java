package com.aspose.cloud.pdf;

public class TextState {
	private double _FontSize;
	public double getFontSize(){return _FontSize;}
	public void setFontSize(double FontSize){_FontSize=FontSize;}

	private String _Font;
	public String getFont(){return _Font;}
	public void setFont(String Font){_Font=Font;}

	private Color _ForegroundColor;
	public Color getForegroundColor(){return _ForegroundColor;}
	public void setForegroundColor(Color ForegroundColor){_ForegroundColor=ForegroundColor;}

	private Color _BackgroundColor;
	public Color getBackgroundColor(){return _BackgroundColor;}
	public void setBackgroundColor(Color BackgroundColor){_BackgroundColor=BackgroundColor;}

	private int _FontStyle;
	public int getFontStyle(){return _FontStyle;}
	public void setFontStyle(int FontStyle){_FontStyle=FontStyle;}

}
